print(1)  # integer value/literal
print(
    type(1)
)  # type of an integer value, in python every thing is an object of a class
print(0.1)  # float value
print(True)  # Boolean value
print(type(True))  # here type is python builtin function
print(1 + 1 + 1 == 3)
print(0.1 + 0.1 + 0.1 == 0.3)
print(0.125 + 0.125 + 0.125 == 0.375)
print(abs((0.1 + 0.1 + 0.1) - 0.3)
      < 0.000001)  # abs means absolute value, here its a function
print(
    format(0.1, ".25f")
)  # format builtin function. Used to show that float numbers in python has bug
print(format(0.125, ".25f"))  # .25 means 25 decimal points after .
